package ex01;

public class MainEntry {
	
	public static void main(String[] args) {
		System.out.print("\nMSA 2th 화이팅!\n");	// 제어문자: \n-줄바꿈, \t-탭, \a-소리남, \b-한칸 앞으로
		System.out.println(100);
		System.out.print(12.34);
		System.out.println();
		System.out.printf("%s", "zoo");		// s는 string, c는 character로 한글자만. f는 float
		System.out.println();
	}   
}
